--CHAPTER12
--첨부파일 관리
--첨부파일을 어떻게 관리하면 되는지 그리고 설계는 어떻게 해야 체계적인 첨부파일을 구축할 수 있는지 확인해 본다.

--1. 일반적인 관리
--1.1.2 첨부파일 업무
--업무 테이블 키
--업무 테이블 키값을 넣어서 사용한다. Primary Key
--만약 업무 테이블의 키값이 두 개 이상 개로 나뉘어져 있다면 결합하여 한 개의 업무 테이블 키에 넣어서 담으면 가능하다.
--첨부파일 같은 경우는 공통으로 사용하는 테이블이므로 보통 릴레이션은 관련 테이블과는 연결하지 않는것이 ERD 가독성에 좋다.

--1.1.3. 첨부파일
--첨부파일일련번호 : 첨부파일 등록 시 생성되는 Primary Key
--첨부파일구분코드 : 업무 테이블 키에 따른 첨부파일의 구분이 될 때 구분코드를 넣어준다.
--PC파일명 : 올리는 PC의 파일명을 적어둔다.
--PC파일확장자 : 파일의 확장자를 넣어둔다.
--파일크기 : 파일의 크기를 넣어둔다.
--서버저장경로 : 서버로 저장 시에 서버의 경로를 넣어둔다.
--서버저장파일명 : 서버 저장 시 파일명을 넣어둔다.
--다운로드횟수 : 조회하고 다운한 횟수를 넣어둔다.
--삭제여부 : 첨부 파일의 물리적 삭제는 없고 삭제 시 'N'으로 마킹을 해둔다.